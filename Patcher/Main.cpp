template <typename T>
void write_pos(streampos pos, T value)
{
	wow_exe.clear();
	wow_exe.seekg(0);
	wow_exe.seekp(pos);
	wow_exe << value;
}

void write_pos(streampos pos, vector<uint8_t> const& values)
{
	wow_exe.clear();
	wow_exe.seekg(0);
	wow_exe.seekp(pos);
	for (uint8_t v : values)
		wow_exe << v;
}

void write_pos_n(streampos pos, uint8_t value, size_t n)
{
	wow_exe.clear();
	wow_exe.seekg(0);
	wow_exe.seekp(pos);
	for (size_t i = 0; i < n; i++)
		wow_exe << value;
}

